package main.java.com.name.javacore.chapter02;

public class ForTest {
    public static void main(String[] args) {
         int x; //Обьвление переменной х, управляющей циклом

         for (x=0; x<10; x++)
             System.out.println("Значение x: " + x); // вывод на экран строки будет просиходить пока не удовлетворится условие цикла

    }
}
