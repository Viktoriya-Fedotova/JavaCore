package main.java.com.name.javacore.chapter04;

public class Modulus {
    public static void main(String[] args) {
        int x = 42;
        double y = 24.25;

        System.out.println("x mod 10 = " + x % 10);
        System.out.println("y mod 10 = " + y % 10);
        System.out.println("x mod y = " + x % y);
    }
}
